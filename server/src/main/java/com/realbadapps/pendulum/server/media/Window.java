package com.realbadapps.pendulum.server.media;

public class Window {
    private long start;
    private long end;

    public Window() {
        //
    }

    /*
        Getters & setters
     */

    public long getStart() {
        return start;
    }

    public void setStart(long start) {
        this.start = start;
    }

    public long getEnd() {
        return end;
    }

    public void setEnd(long end) {
        this.end = end;
    }
}